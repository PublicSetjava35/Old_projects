package org.example.ClassPractitioner.abstracts;

public abstract class KeyBoard {
    public abstract void W();
    public abstract void A();
    public abstract void S();
    public abstract void D();
    public abstract int keyY();
    public abstract int keyX();
    public abstract void oldX(int oldX);
    public abstract void oldY(int oldY);
    public abstract void frameX(int width);
    public abstract void frameY(int height);
}